	private static void merge(int[] arr, int low, int middle, int high) {

		int[] L = new int[middle - low + 1];
		int[] R = new int[high - middle];

		int n1 = L.length;
		int n2 = R.length;
		for (int i = 0; i < n1; i++) {
			L[i] = arr[i + low];
		}

		for (int i = 0; i < n2; i++) {
			R[i] = arr[i + middle + 1];
		}

		int iL = 0, iR = 0;

		int origStart = low;
		while (iL < n1 && iR < n2) {
			if (L[iL] < R[iR]) {
				arr[origStart] = L[iL];
				iL++;
			} else {
				arr[origStart] = R[iR];
				iR++;
			}
			origStart++;
		}

		while (iL < n1) {
			arr[origStart] = L[iL];
			origStart++;
			iL++;
		}

		while (iR < n2) {
			arr[origStart] = R[iR];
			origStart++;
			iR++;
		}

	}

	public static int inversionCount(int arr[], int low, int high) {
		int invCount = 0;
		if (high > low) {
			int middle = (low + high) / 2;
			invCount = inversionCount(arr, low, middle);
			invCount += inversionCount(arr, middle + 1, high);

			invCount += mergeInv(arr, low, middle, high);
		}

		return invCount;

	}

	private static int mergeInv(int[] arr, int low, int middle, int high) {
		int n1 = middle - low + 1;
		int n2 = high - middle;
		int invCount = 0;

		int L[] = new int[n1];
		int R[] = new int[n1];

		for (int i = 0; i < n1; i++) {
			L[i] = arr[low + i];
		}

		for (int i = 0; i < n2; i++) {
			R[i] = arr[middle + i + 1];
		}

		int i = 0, j = 0;

		int k = low;
		while (i < n1 && j < n2) {
			if (L[i] < R[j]) {
				arr[k++] = L[i++];
			} else {
				arr[k++] = R[j++];
				invCount += (n1 - i);
			}
		}

		while (i < n1) {
			arr[k++] = L[i++];
		}
		while (j < n2) {
			arr[k++] = R[j++];
		}

		return invCount;

	}

	public static void quickSort(int[] arr, int low, int high) {
		if (low < high) {

			int partitionIndex = partition(arr, low, high);
			quickSort(arr, low, partitionIndex - 1);
			quickSort(arr, partitionIndex + 1, high);
		}
	}

	private static int partition(int[] arr, int low, int high) {
		int pivot = arr[high];
		int index = low - 1;
		for (int i = low; i < high; i++) {
			if (arr[i] <= pivot) {
				index++;
				swap(arr, i, index);
			}
		}
		index++;
		swap(arr, index, high);
		return index;
	}

