	private static int partitionPractise(int[] arr, int low, int high) {
		int pivot = arr[high];
		int index = low - 1;
		int i = low;
		while (i < high) {
			if (arr[i] < pivot) {
				index++;
				swap(arr, i, index);
			}
			i++;
		}
		index++;
		swap(arr, index, high);
		return index;
	}

	private static int partitionPractise(int[] arr, int low, int high, int pivot) {

		for (int i = 0; i < arr.length; i++) {
			if (arr[i] == pivot) {
				swap(arr, i, high);
				break;
			}
		}
		int index = low - 1;
		int i = low;
		while (i < high) {
			if (arr[i] < pivot) {
				index++;
				swap(arr, i, index);
			}
			i++;
		}
		index++;
		swap(arr, index, high);
		return index;
	}

