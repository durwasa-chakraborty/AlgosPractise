	private static void bubbleSort(int[] arr) {

		if (arr.length == 1 || arr.length == 0) {
			return;
		}

		boolean isOneMovementDone = true;

		while (isOneMovementDone) {
			isOneMovementDone = false;
			for (int i = 0; i < arr.length - 1; i++) {
				if (arr[i] > arr[i + 1]) {
					int temp = arr[i];
					arr[i] = arr[i + 1];
					arr[i + 1] = temp;
					isOneMovementDone = true;

				}
			}
		}
	}

	private static void insertionSort(int arr[]) {
		if (arr.length == 0 || arr.length == 1) {
			return;
		}
		int j;
		for (int i = 1; i < arr.length; i++) {
			j = i - 1;
			int item = arr[i];
			while (j >= 0 && item < arr[j]) {
				arr[j + 1] = arr[j];
				j--;
			}
			arr[j + 1] = item;
		}
	}

	private static void binaryInsertionSort(int arr[]) {
		if (arr.length == 0 || arr.length == 1) {
			return;
		}
		for (int i = 0; i < arr.length; i++) {
			int j = i - 1;
			int item = arr[i];
			int pos = binarySearchForInsertionSort(arr, item, 0, j);

			while (j >= pos) {
				arr[j + 1] = arr[j];
				j--;
			}

			arr[j + 1] = item;
		}

	}

	private static int binarySearchForInsertionSort(int[] arr, int item, int low, int high) {
		if (high <= low) {
			return (item > arr[low]) ? low + 1 : low;
		}
		int middle = (low + high) / 2;

		if (item == arr[middle]) {
			return middle + 1;
		}

		if (item < arr[middle]) {
			return binarySearchForInsertionSort(arr, item, low, middle - 1);
		}
		return binarySearchForInsertionSort(arr, item, middle + 1, high);
	}

